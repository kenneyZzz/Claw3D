interface I18nContextValue {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: string, params?: Params) => string;
}

const I18nContext = createContext<I18nContextValue>({
  locale: 'zh',
  setLocale: () => {},
  t: (key) => key,
});

const formatWithParams = (template: string, params?: Params) => {
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (_, token: string) => {
    const value = params[token];
    return value === undefined ? '' : String(value);
  });
};

const getInitialLocale = (): Locale => {
  if (typeof window === 'undefined') return 'zh';

  const saved = window.localStorage.getItem(LOCALE_STORAGE_KEY);
  if (saved === 'zh' || saved === 'en') return saved;

  const legacySaved = window.localStorage.getItem(LEGACY_OFFICE_LOCALE_KEY);
  if (legacySaved === 'zh' || legacySaved === 'en') return legacySaved;

  const browserLocale = window.navigator.language.toLowerCase();
  return browserLocale.startsWith('zh') ? 'zh' : 'en';
};

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(getInitialLocale);

  const setLocale = useCallback((nextLocale: Locale) => {
    setLocaleState(nextLocale);
    window.localStorage.setItem(LOCALE_STORAGE_KEY, nextLocale);
  }, []);

  const t = useCallback(
    (key: string, params?: Params) => {
      const text = translations[locale]?.[key] ?? translations.zh[key] ?? key;
      return formatWithParams(text, params);
    },
    [locale],
  );

  const value = useMemo(
    () => ({ locale, setLocale, t }),
    [locale, setLocale, t],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  return useContext(I18nContext);
}

export function LanguageSwitcher() {
  const { locale, setLocale, t } = useI18n();

  return (
    <select
      value={locale}
      onChange={(event) => setLocale(event.target.value as Locale)}
      aria-label={t('office.language.label')}
      title={t('office.language.label')}
      className="rounded border border-amber-700/60 bg-black/70 px-1.5 py-0.5 text-sm text-amber-200 outline-none"
    >
      <option value="zh">{t('office.language.zh')}</option>
      <option value="en">{t('office.language.en')}</option>
    </select>
  );
}
